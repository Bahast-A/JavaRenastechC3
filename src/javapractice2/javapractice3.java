public class javapractice3 {
    public static void main(String[] args) {
        int number;
        number = 2;
        String result = "";
        if (number % 2 == 0) {
            result = number + " is a Even number";

        } else {
            result = number + " is a Odd";
        }
        System.out.println(result);
    }
}
