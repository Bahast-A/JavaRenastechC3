package day3_Continuation;

public class c1_booleanCharContinue {

    public static void main(String[] args){
        boolean b1=false;
        boolean b2=25 == 30;
        boolean b3= 20 != 21;
        System.out.println(b2);
        System.out.println(b3);

        System.out.println(true == false);  // thats false because true isnt equal to false
        System.out.println(!true == false);  // the ! makes it opposite. so this statement is true

        char c1='b'; char c2='a'; char c3='h'; char c4='s'; char c5='t';
        System.out.println(c1);

    }
}
