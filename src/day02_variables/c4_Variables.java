package day02_variables;

public class c4_Variables {

    public static void main(String[] args){
//we use the specific reserved ranges to save memory
// byte = (-128 to 127) short = (-32,768 to 32,767)
// int = (-2.1b to 2.1b) long = (-9.2b to 9.2b)
// float = 6 to 7 decimal points - double = stores 15 decimal digits
// boolean = stores true or false
//char = stores a single character

        byte number1=25;
        System.out.println(number1);
        byte number2=-100;
        System.out.println(number2);
        short number3=120;
        short number4=20000;
        int number5=40000;
        short number6=50;
        long number7=100000L; //when you move out of int range you have to add an L to the end
        System.out.println(number7);









    }






}
