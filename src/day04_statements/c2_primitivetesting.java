package day04_statements;

public class c2_primitivetesting {
    public static void main(String[] args) {
        //2 types of testing implicit and explicit casting
        byte b1 = 20;
        long l1 = b1;//this is implicit casting - done by java automatically
        int i1 = 10;
        byte b2 = (byte) i1; //explicit casting - this will be done by you manually
        double d1 = 2.3;
        float f1 = (float) d1;//explicit casting
        int i2 = 200;
        long l2 = i2; //implicit casting
        short s = 155;


    }
}
