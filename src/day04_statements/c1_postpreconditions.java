package day04_statements;

public class c1_postpreconditions {
    public static void main(String[] args) {
        byte x = 21;
        int w = 20;
        System.out.println(x);
        System.out.println(--x);
        System.out.println(w--);
        System.out.println(w--);
        System.out.println(--w);
        System.out.println(w);
        System.out.println(w++);
        System.out.println(w);
    }
}
